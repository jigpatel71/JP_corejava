package com.test.constructors;

public class ConstructorNotes {
	
	/*
	 * constructors -
	 *  used to create an instance of the class 
	 * 	have the same name as class, 
	 * 	have no return type, 
	 * 	have public access modifiers, they take private too.
	 *  can be overloaded constructors
	 *  compiler provides the default constructor (no argument constructor) if no other constr. is defined
	 * 
	 *  super() - calling superclass constructor. This is always plugged in by the compiler, irrespective of
	 *  					whether it is called or not
	 *  super(parameters) - calling super class constructor which has parameters
	 * 
	 *  this() - calling current class constructor
	 *  this(arguments) - calling current class constructor which has parameters
	 *  
	 *  you cannot use both super() + this() in your constructor. You can only use one of them.
	 *  
	 *  super.method()  --> this is invoking super class method from child
	 *  this.method() --> this is invoking a method from current class
	 *  
	 * 
	 */
	
	

}
